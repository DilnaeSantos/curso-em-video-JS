// auto atribuições
var n = 5
n = n + 4 //agora n vale 9
n = n - 3 // 6
n = n * 2 // 12
n = n / 4 // 3
n = n ** 2 // 9
n = n % 3 // 0
// simplificando só funciona se for a mesma variavel
n += 4 // 9
n -= 3 // 6
n *= 2 // 12
n /= 4 // 3
n **= 2 // 9
n %= 3 // 0
